// - **3: 공포 영화 - 9000원**

// **나이에 따른 할인율은 다음과 같습니다:**

// - **13세 미만: 50% 할인**
// - **60세 이상: 30% 할인**
// **사용자의 나이와 선택한 영화에 따라 할인을 적용한 최종 티켓 가격을 계산하여 출력하는 프로그램을 작성하세요.**
